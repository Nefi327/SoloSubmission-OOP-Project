    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String selected = lstPledges.getSelectedValue();
        
        if (selected == null){
            JOptionPane.showMessageDialog(this,
                    "Please select a pledge to delete.",
                    "No selection",
                    javax.swing.JOptionPane.WARNING_MESSAGE
            );
            return;
        }
        
        //extract full name: everything before " - ("
        int cutIndex = selected.indexOf(" - (");
        String name;
        if (cutIndex > 0){
            name = selected.substring(0, cutIndex).trim();
        } else {
            name = selected.trim(); //fallback
        }
       
        boolean removed = pledgeManager.removeByName(name);
        
        if (removed){
            JOptionPane.showMessageDialog(this,
                    "Pledge deleted successfully,",
                    "Deleted",
                    JOptionPane.INFORMATION_MESSAGE);
            refreshPledgeList(); //reload list
        } else {
            JOptionPane.showMessageDialog(this,
                    "Could not deleted pledge.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String name = txtSearchName.getText().trim();
        
        if (name.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(
            this,
                    "Please enter a name to serach.",
                    "No name entered",
                    javax.swing.JOptionPane.WARNING_MESSAGE
            );
            return;
        }
        
        //using pledgemanager search function
        Pledge found = pledgeManager.findByName(name);
        
        if (found == null) {
            javax.swing.JOptionPane.showMessageDialog(
            this,
                    "No pledge found for: " + name,
                    "Not found",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE
            );
            return;
        }
        
        //show the found pledge
        javax.swing.JOptionPane.showMessageDialog(
                this,
                "Pledge found:\n" + found.toString(),
                "Pledge found",
                javax.swing.JOptionPane.INFORMATION_MESSAGE
        );
        
        //highlist in the JList
        javax.swing.ListModel<String> model = lstPledges.getModel();
        int indexToSelect = -1;
        
        for (int i = 0; i < model.getSize(); i++) {
            String item = model.getElementAt(i);
            if (item.startsWith(found.getUserName()+ " - (:")) {
                indexToSelect = i;
                break;
            }
        }
        
        if (indexToSelect >= 0){
            lstPledges.setSelectedIndex(indexToSelect);
            lstPledges.ensureIndexIsVisible(indexToSelect);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

