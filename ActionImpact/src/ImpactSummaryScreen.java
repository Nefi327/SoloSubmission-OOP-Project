    //updates the JList
    private void refreshPledgeList(){
        if (pledgeManager == null) return;
        
        ArrayList<Pledge> allPledges = pledgeManager.getAllPledge();
        DefaultListModel<String> model = new DefaultListModel<>();
        
        for (Pledge p : allPledges){
            model.addElement(p.toString()); 
        }
        
        lstPledges.setModel(model);
        
        //updating top stats
        int total = allPledges.size();
        lblTotalPledgesValue.setText("" + total);
        
        //estimate of plastic reduced
        double estimatedKg = total * 0.5;
        String formatted = String.format("%.1f", estimatedKg);
        lblPlasticReducedValue.setText("" + formatted);
    }

